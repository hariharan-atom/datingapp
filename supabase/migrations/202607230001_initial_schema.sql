-- Mirror auth users into application users.
create or replace function public.handle_new_auth_user()
returns trigger
language plpgsql
security definer set search_path = ''
as $$
begin
  insert into public.users(id, phone, email)
  values(new.id, new.phone, new.email)
  on conflict(id) do nothing;
  return new;
end;
$$;

create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_auth_user();

-- Match creation is transactional and deterministic.
create or replace function public.create_match_after_like()
returns trigger
language plpgsql
security definer set search_path = ''
as $$
declare
  new_match_id uuid;
  new_chat_id uuid;
  left_user uuid;
  right_user uuid;
begin
  if exists (
    select 1 from public.likes
    where actor_user_id = new.target_user_id
      and target_user_id = new.actor_user_id
  ) then
    left_user := least(new.actor_user_id, new.target_user_id);
    right_user := greatest(new.actor_user_id, new.target_user_id);

    insert into public.matches(user_a_id, user_b_id, initiated_by)
    values(left_user, right_user, new.actor_user_id)
    on conflict(user_a_id, user_b_id)
    do update set unmatched_at = null
    returning id into new_match_id;

    insert into public.chats(match_id)
    values(new_match_id)
    on conflict(match_id) do update set is_archived = false
    returning id into new_chat_id;

    insert into public.chat_members(chat_id, user_id)
    values(new_chat_id, left_user), (new_chat_id, right_user)
    on conflict do nothing;

    insert into public.notifications(user_id, actor_user_id, type, title, data)
    values
      (left_user, right_user, 'match', 'It''s a match!', jsonb_build_object('match_id', new_match_id)),
      (right_user, left_user, 'match', 'It''s a match!', jsonb_build_object('match_id', new_match_id));
  end if;
  return new;
end;
$$;

create trigger on_like_create_match
  after insert or update on public.likes
  for each row execute function public.create_match_after_like();

