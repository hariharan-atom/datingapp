    user_id = auth.uid()
    and exists (
      select 1 from public.groups g
      where g.id = group_members.group_id and g.privacy = 'public'
    )
  );
create policy "members leave groups" on public.group_members for delete
  using (user_id = auth.uid());
create policy "group posts visible to members or public" on public.group_posts for select
  using (
    exists (
      select 1 from public.groups g
      where g.id = group_posts.group_id and g.privacy = 'public'
    )
    or exists (
      select 1 from public.group_members gm
      where gm.group_id = group_posts.group_id and gm.user_id = auth.uid()
    )
  );
create policy "members create posts" on public.group_posts for insert
  with check (
    author_user_id = auth.uid()
    and exists (
      select 1 from public.group_members gm
      where gm.group_id = group_posts.group_id and gm.user_id = auth.uid()
    )
  );
create policy "authors update posts" on public.group_posts for update
  using (author_user_id = auth.uid()) with check (author_user_id = auth.uid());

