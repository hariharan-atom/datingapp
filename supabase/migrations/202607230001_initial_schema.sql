create policy "public profile photos" on storage.objects for select
  using (bucket_id = 'profile-photos');
create policy "users upload profile photos to own folder" on storage.objects for insert
  with check (
    bucket_id = 'profile-photos'
    and (storage.foldername(name))[1] = auth.uid()::text
  );
create policy "users manage own profile photos" on storage.objects for update
  using (
    bucket_id = 'profile-photos'
    and owner_id = auth.uid()::text
  );
create policy "users delete own profile photos" on storage.objects for delete
  using (
    bucket_id = 'profile-photos'
    and owner_id = auth.uid()::text
  );

create policy "chat members read chat media" on storage.objects for select
  using (
    bucket_id = 'chat-media'
    and exists (
      select 1 from public.chat_members cm
      where cm.chat_id::text = (storage.foldername(name))[1]
        and cm.user_id = auth.uid()
    )
  );
create policy "chat members upload chat media" on storage.objects for insert
  with check (
    bucket_id = 'chat-media'
    and exists (
      select 1 from public.chat_members cm
      where cm.chat_id::text = (storage.foldername(name))[1]
        and cm.user_id = auth.uid()
    )
  );
create policy "verification media self" on storage.objects for all
  using (
    bucket_id = 'verification'
    and (storage.foldername(name))[1] = auth.uid()::text
  )
  with check (
    bucket_id = 'verification'
    and (storage.foldername(name))[1] = auth.uid()::text
  );

