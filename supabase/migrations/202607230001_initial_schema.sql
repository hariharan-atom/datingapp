-- Discovery RPC: exact coordinates remain private.
create or replace function public.get_discovery_profiles(
  max_distance_km numeric default 50,
  min_age integer default 18,
  max_age integer default 99,
  verified_only boolean default false
)
returns table (
  user_id uuid,
  display_name text,
  age integer,
  bio text,
  occupation text,
  city text,
  distance_km numeric,
  is_verified boolean,
  profile_score integer
)
language sql
stable
security definer
set search_path = ''
as $$
  select
    p.user_id,
    p.display_name,
    extract(year from age(p.birth_date))::integer as age,
    p.bio,
    p.occupation,
    l.city,
    round(
      (
        extensions.st_distance(
          l.point,
          viewer.point
        ) / 1000
      )::numeric,
      1
    ) as distance_km,
    p.is_verified,
    p.profile_score
  from public.profiles p
  join public.users u on u.id = p.user_id
  left join public.locations l on l.user_id = p.user_id
  left join public.locations viewer on viewer.user_id = auth.uid()
  where p.user_id <> auth.uid()
    and p.is_discoverable
    and not p.is_incognito
    and u.is_active
    and extract(year from age(p.birth_date)) between min_age and max_age
    and (not verified_only or p.is_verified)
    and (
      viewer.point is null
      or l.point is null
      or extensions.st_dwithin(l.point, viewer.point, max_distance_km * 1000)
    )
    and not exists (
      select 1 from public.blocks b
      where (b.blocker_user_id = auth.uid() and b.blocked_user_id = p.user_id)
         or (b.blocker_user_id = p.user_id and b.blocked_user_id = auth.uid())
    )
    and not exists (
      select 1 from public.profile_actions a
      where a.actor_user_id = auth.uid()
        and a.target_user_id = p.user_id
        and a.action in ('pass', 'hide')
    )
  order by
    case when viewer.point is null or l.point is null then 1 else 0 end,
    distance_km asc nulls last,
    p.profile_score desc;
$$;

grant execute on function public.get_discovery_profiles(numeric, integer, integer, boolean)
to authenticated;

